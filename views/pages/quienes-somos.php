                <p class="mb-5 fs-4">
                    Hoy en día, la Industria Militar no solo apoya al Ejército de Guatemala, sino que también beneficia
                    a otras entidades gubernamentales, desempeñando un papel crucial en el desarrollo y la seguridad del
                    país. Su contribución es vital para el cumplimiento de las misiones militares, proporcionando equipo
                    necesario y manteniendo altos estándares de calidad y profesionalismo, lo que refuerza su reputación
                    como una entidad clave dentro de la infraestructura de defensa nacional.
                </p>
