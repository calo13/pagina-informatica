            <div class="text-justify mx-auto wow animate__animated animate__fadeInLeft" data-wow-delay="0.1s"
                style="text-align: justify;">
                <p class="mb-5 fs-4">
                    La Industria Militar de Guatemala tiene sus orígenes en el siglo XIX, específicamente en 1830,
                    cuando el Capitán José Mariano Morales solicitó la confección de uniformes para el escuadrón No. 7.
                    Este evento marcó el inicio de un proceso de estandarización en la vestimenta militar, impulsando la
                    necesidad de uniformidad en el Ejército guatemalteco.
                </p>
            </div>

            <div class="text-justify mx-auto wow animate__animated animate__fadeInLeft" data-wow-delay="0.1s"
                style="text-align: justify;">
                <p class="mb-5 fs-4">
                    Durante el período Liberal, en 1879, se publicó la Ordenanza Militar, que describía la vestimenta
                    del personal militar, influenciada en gran parte por diseños españoles. Aunque esta normativa sentó
                    las bases para unificar criterios de vestimenta, el Ejército carecía de una unidad dedicada a la
                    confección de uniformes, lo que llevó a depender de la adquisición de equipo en el extranjero.
                </p>
            </div>

            <div class="text-justify mx-auto wow animate__animated animate__fadeInLeft" data-wow-delay="0.1s"
                style="text-align: justify;">
                <p class="mb-5 fs-4">
                    Entre 1884 y 1896, la Secretaría de la Guerra empezó a realizar contratos con sastres para cumplir
                    con las especificaciones normativas. Por ejemplo, se autorizó a la Casa Dernier de París para
                    confeccionar cien uniformes a un costo significativo de 7,350 francos. Este periodo evidenció un
                    incremento en la organización y formalización de la producción de uniformes.
                </p>
            </div>

            <div class="text-justify mx-auto wow animate__animated animate__fadeInLeft" data-wow-delay="0.1s"
                style="text-align: justify;">
                <p class="mb-5 fs-4">
                    En el primer tercio del siglo XX, específicamente en 1933, se promulgó un reglamento que no solo
                    especificaba el diseño y color de los uniformes, sino que también establecía los materiales a
                    utilizar, promoviendo un equilibrio en la confección. A partir de 1936, se creó el Servicio de
                    Intendencia, encargado de proporcionar recursos en metálico, alimentos y vestuario, aunque
                    inicialmente no se especificaba quiénes eran los encargados de confeccionar la vestimenta.
                </p>
            </div>

            <div class="text-justify mx-auto wow animate__animated animate__fadeInLeft" data-wow-delay="0.1s"
                style="text-align: justify;">
                <p class="mb-5 fs-4">
                    La formalización de la Industria Militar como entidad comenzó tras la Revolución del 20 de Octubre
                    de 1944. En 1945, se estableció el Departamento del Cuartel Maestre y el Departamento de Maestranza,
                    que asumieron la responsabilidad de adquisición, almacenamiento y distribución de vestuario y
                    equipo. A partir de 1947, se intentó establecer una fábrica de uniformes, aunque inicialmente
                    funcionó solo como punto de distribución.
                </p>
            </div>

            <div class="text-justify mx-auto wow animate__animated animate__fadeInLeft" data-wow-delay="0.1s"
                style="text-align: justify;">
                <p class="mb-5 fs-4">
                    En 1983, se emitió el Decreto Ley No. 29-83, que formalizó la creación de la Industria Militar como
                    una dependencia del Estado Mayor de la Defensa Nacional, con un enfoque claro en la confección y
                    comercialización de vestuario y equipo militar. Este decreto incluyó la creación de la Fábrica de
                    Municiones del Ejército en Cobán, Alta Verapaz.
                </p>
            </div>

            <div class="text-justify mx-auto wow animate__animated animate__fadeInLeft" data-wow-delay="0.1s"
                style="text-align: justify;">
                <p class="mb-5 fs-4">
                    Durante la década de 1990, la Industria Militar fue reconocida como una dependencia independiente,
                    ampliando su capacidad de comercialización a otras entidades estatales y promoviendo un modelo de
                    autosuficiencia. En 1992, se formalizó su independencia del Servicio de Intendencia del Ejército,
                    permitiendo un enfoque más diversificado en la producción y comercialización de productos.
                </p>
            </div>

            <div class="text-justify mx-auto wow animate__animated animate__fadeInLeft" data-wow-delay="0.1s"
                style="text-align: justify;">
                <p class="mb-5 fs-4">
                    A lo largo de los años, la Industria Militar ha recibido numerosos premios por la calidad de su
                    producción, incluyendo reconocimientos a nivel nacional como la Ceiba de Oro y premios
                    internacionales como el Clio. En 2008, se realizó una modernización significativa de sus
                    instalaciones, mejorando la atención al público y la calidad de los productos ofrecidos.
                </p>
            </div>

